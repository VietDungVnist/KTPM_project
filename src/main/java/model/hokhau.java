package model;

public class hokhau {
	String shk;
	int id_chuho, count;
	String diachi;
	
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	public String getShk() {
		return shk;
	}
	public void setShk(String shk) {
		this.shk = shk;
	}
	public int getId_chuho() {
		return id_chuho;
	}
	public void setId_chuho(int id_chuho) {
		this.id_chuho = id_chuho;
	}
	public String getDiachi() {
		return diachi;
	}
	public void setDiachi(String diachi) {
		this.diachi = diachi;
	}
}
